
public class Event {
	
	String id;
	String city;
	String description;
	String date;
	int availableNumberOfparticipants;
	
	Event(String id, String city, String description, String date, int availableNumberOfparticipants){
		this.id = id;
		this.city = city;
		this.description = description;
		this.date = date;
		this.availableNumberOfparticipants = availableNumberOfparticipants;
		
		
	}
}
